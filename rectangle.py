
def area(a, b):
    '''Функция находит площадь четырехугольника и принимае 2 аргумента 
    а (int) и b (int). Ф-я возвращает a*b (int)'''
    return a * b


def perimeter(a, b):
    '''Функция находит периметр четырехугольника и принимае 2 аргумента 
    а (int) и b (int). Ф-я возвращает 2(a+b) (int)'''
    return 2*(a + b)
