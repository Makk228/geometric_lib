def area(a, b):
    """Функция находит площадь четырехугольника и принимает 2 аргумента 
    a (int) и b (int). Функция возвращает a * b (int).
    """
    return a * b


def perimeter(a, b):
    """Функция находит периметр четырехугольника и принимает 2 аргумента 
    a (int) и b (int). Функция возвращает 2 * (a + b) (int).
    """
    return 2 * (a + b)
