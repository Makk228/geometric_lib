def area(a, h):
    '''Функция принимает 2  аргумента a, h (все int) и возвращает
        площадь тркугольника по формуле 1/2*a*h (float)
    '''
    return  1/2*a*h


def perimeter(a, b, c):
    ''' Функция принимает 3  аргумента a b c (все int) и возвращает
        периметр треугольника по формуле a + b + c (int).
    '''
    return a + b + c
    
